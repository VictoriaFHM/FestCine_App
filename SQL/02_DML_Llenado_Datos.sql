/* Actualización de aforo después de cargar entradas y abonos. */
UPDATE P
SET P.AforoDisponibleActual =
    S.CapacidadAsientos
    - ISNULL(E.CantidadEntradas, 0)
    - ISNULL(A.CantidadAbonos, 0)
FROM Proyeccion P
INNER JOIN Sala S ON P.IdSala = S.IdSala
LEFT JOIN
(
    SELECT IdProyeccion, COUNT(*) AS CantidadEntradas
    FROM Entrada
    GROUP BY IdProyeccion
) E ON P.IdProyeccion = E.IdProyeccion
LEFT JOIN
(
    SELECT IdProyeccion, COUNT(*) AS CantidadAbonos
    FROM AbonoProyeccion
    GROUP BY IdProyeccion
) A ON P.IdProyeccion = A.IdProyeccion;

UPDATE EP
SET EP.AforoDisponible =
    EP.AforoMax - ISNULL(EE.CantidadEntradasEvento, 0)
FROM EventoParalelo EP
LEFT JOIN
(
    SELECT IdEvento, COUNT(*) AS CantidadEntradasEvento
    FROM EntradaEvento
    GROUP BY IdEvento
) EE ON EP.IdEvento = EE.IdEvento;

